number_1 = float(input("Informe o Primeiro Numero: "))
number_2 = float(input("Informe o Segundo Numero: "))

if number_1 > number_2:
    print("O Primeiro Numero e Maior!")
    print("A Diferenca entre ele e: ", number_1 - number_2)
elif number_2 > number_1:
    print("O Segundo Numero e Maior!")
    print("A Diferenca entre ele e: ", number_2 - number_1)
